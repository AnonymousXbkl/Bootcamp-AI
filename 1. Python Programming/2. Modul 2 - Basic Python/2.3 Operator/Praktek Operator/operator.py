# Opertator Aritmatika
print("\n=====================Operator Aritmatika========================")

a = 5
b = 3
print(a + b) # Tambah
print(a - b) # Kurang
print(a * b) # Kali
print(a / b) # Bagi
print(a % b) # Modulus (sisa bagi)
print(a ** b) # Pangkat
print(a // b) # Pembulatan kebawah

print("\n")

a += 3
print(a)

s = "Andi "
print(s * 3) # Mengulang string

b = True
print(b/2) # True = 1, False = 0

# Operator Perbandingan =============================================================
print("\n=====================Operator Perbandingan========================")

x = 10
y = 15

print(x == y) # Sama dengan
print(x != y) # Tidak sama dengan
print(x > y) # Lebih besar dari
print(x < y) # Lebih kecil dari
print(x >= y) # Lebih besar sama dengan
print(x <= y) # Lebih kecil sama dengan
print (x is y) # Objek identik
print (x is not y) # Objek tidak identik


















